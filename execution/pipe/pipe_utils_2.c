#include "../../minishell.h"

int	ft_pipe(t_args *cmd, int *fd1, int *fd2, int i)
{
	if (i == pipe_counter(cmd))
	{
		pipe(fd1);
		i--;
	}
	if (i > 0 && cmd->type == PIPE)
	{
		pipe(fd2);
		i--;
	}
	return (i);
}
